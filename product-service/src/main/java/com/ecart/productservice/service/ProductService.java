package com.ecart.productservice.service;

import com.ecart.productservice.dto.ProductRequest;
import com.ecart.productservice.dto.ProductResponse;
import com.ecart.productservice.model.Product;
import com.ecart.productservice.repository.ProductRepository;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Service;

import java.util.List;

@Service
@RequiredArgsConstructor                // this helps by creating required constructor on compile time
@Slf4j
public class ProductService {

    private final ProductRepository productRepository;    //Making use of Constructor
                                                            // Injection to inject ProductRepository
    public void createProduct(ProductRequest productRequest){
        Product product = Product.builder()
                .name(productRequest.getName())
                .description(productRequest.getDescription())
                .price(productRequest.getPrice())
                .build();

        productRepository.save(product);
        log.info("Product {} is saved ",product.getId());
    }

    public List<ProductResponse> getAllProducts() {
        List<Product> products = productRepository.findAll();
        return products.stream().map(this::mapToProductResponse).toList();
    }

    private ProductResponse mapToProductResponse(Product product) {
        return ProductResponse.builder()
                .id(product.getId())
                .name(product.getName())
                .description(product.getDescription())
                .price(product.getPrice())
                .build();
    }
}
